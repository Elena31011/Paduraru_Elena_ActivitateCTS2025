package clase;

public class Pat {
    private int nrPat;
    private boolean esteLiber;

    public Pat(int nrPat, boolean esteLiber) {
        this.nrPat = nrPat;
        this.esteLiber = esteLiber;
    }

    public int getNrPat() {
        return nrPat;
    }

    public boolean isEsteLiber() {
        return esteLiber;
    }
}
